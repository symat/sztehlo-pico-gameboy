code     segment
         assume    cs:code,ds:code
         org       100h

minimaze:

; ------ initialize random number generator

         push      ds
         xor       ax,ax
         mov       ds,ax			; DS <- 0
         mov       ax,ds:[46ch]			; system timer
         pop       ds
         mov       word ptr ds:[randomr],ax

; ------ initialize videomode

         mov       ah,0fh
         int       10h				; get current videomode
         mov       ds:[VMod],al
         cmp       al,7				; MDA ?
         je        blud1
         mov       word ptr ds:[VRAM],0b800h	; CGA video segment
         mov       byte ptr ds:[byteline],80	; 40 characters per row
         mov       al,1				; required videomode 40x25
blud1:   mov       ah,0
         int       10h				; initialize videomode

; ------ start new game - draw maze

gener:   mov       si,offset mapa1
         call      random0
         and       dx,1fh
         cmp       dx,22
         jae       gener
         mov       ax,offset(mapa2-mapa1)
         mul       dx
         add       si,ax

         mov       cx,23			; number of rows
         mov       dx,100h			; start position
gener2:  push      cx
         push      dx
         mov       cx,5				; number of bytes
gener3:  push      cx
         mov       cx,8				; number of bits
         mov       bl,ds:[si]
         inc       si
gener4:  shl       bl,1
         mov       ax,word ptr ds:[znakv0]	; free square not visible
         jnc       gener5			; square is free
         mov       ax,word ptr ds:[znakp0]
gener5:  call      writech			; draw character on dispaly
         loop      gener4
         pop       cx
         loop      gener3			; next byte
         pop       dx
         pop       cx
         inc       dh				; next row
         loop      gener2

; ------ draw maze frame

ram:     xor       dx,dx			; start position of the frame
         mov       ax,word ptr ds:[znakram]	; frame character
         call      linkaram			; draw top row of frame
         inc       dh				; 2nd row
         mov       cx,23			; number of inner rows
ram1:    xor       dl,dl			; start column
         call      writech			; draw left border of frame
         add       dl,38			; right border
         call      writech			; draw right border of frame
         inc       dh				; increase row
         loop      ram1				; draw next row
         call      linkaram			; draw bottom border of frame

; ------ init start game

         mov       dx,2*256
         mov       ds:[pozice],dx		; start position
         inc       dl
         mov       ax,word ptr ds:[znakv0]
         call      writech
         mov       dx,22*256+38			; position of right bottom edge
         call      writech
         mov       ax,word ptr ds:[znakcil]	; destination character
         call      writech			; draw destination

; ------ wait for a key press

	; draw player
vstup:   mov       ax,word ptr ds:[znakh0]	; player character 1
         test      word ptr ds:[citackur],2000h	; cursor color
         jz        vstup1			; 1st color
         mov       ax,word ptr ds:[znakh1]	; player character 2
vstup1:  mov       dx,ds:[pozice]		; player's position
         call      writech			; draw player
         call      kurzout			; draw cursor off

	; wait for a key
vstup2:  inc       word ptr ds:[citackur]	; increase cursor counter
         test      word ptr ds:[citackur],1fffh	; change color?
         jz        vstup			; change color
         int       28h				; server resident DOS functions
         mov       ah,1
         int       16h				; test key
         jz        vstup2			; key not ready
         xor       ax,ax
         int       16h				; get key

; ------ player's move

	; break
         cmp       al,3				; Ctrl+C ?
         je        posun1
         or        ax,ax			; Ctrl+Break?
         jz        posun1
         cmp       al,27			; Esc?
         jne       posun2
posun1:  mov       al,ds:[vmod]			; old videomode
         xor       ah,ah
         int       10h				; return old videomode

         int       20h				; quit program

	; shift left
posun2:  mov       dx,ds:[pozice]		; player's position
         or        dl,dl
         jz        posun3
         cmp       ah,4bh			; cursor left
         jne       posun3
         dec       dl

	; shift right
posun3:  cmp       ah,4dh			; cursor right
         jne       posun4
         inc       dl

	; shift up
posun4:  cmp       ah,48h			; cursor up
         jne       posun5
         dec       dh

	; shift down
posun5:  cmp       ah,50h			; cursor down
         jne       posun6
         inc       dh

	; check free position
posun6:  call      testch			; check character on new position
         jc        vstup			; not empty square

; ------ new position is valid - do shift

posun8:  push      ax
         push      dx				; push new position
         mov       ax,word ptr ds:[znakv1]	; free square visible
         mov       dx,ds:[pozice]		; old position of the player
         call      writech			; clear old position
         pop       dx				; pop new position
         pop       ax

         mov       ds:[pozice],dx		; set new position

; ------ check destination

         cmp       ax,word ptr ds:[znakcil]	; is it destination character?
         jne       okoli			; no

	; show whole maze
         mov       dx,101h
         mov       cx,23
ukvse1:  push      cx
         push      dx
         mov       cx,38
ukvse2:  call      ukaz				; show whole maze
         inc       dl
         loop      ukvse2
         pop       dx
         pop       cx
         inc       dh
         loop      ukvse1

	; destination reached
         mov       dx,10			; position to print text
         mov       si,offset ciltxt		; destination text
         mov       ah,ds:[barvacil]
         call      writetxt
         call      kurzout
         xor       ax,ax
         int       16h				; wait for a key
         jmp       gener			; new game

; ------ display of surrounding squares

okoli:   dec       dl
         call      ukaz				; show swuare left
         add       dl,2
         call      ukaz				; show square right
         dec       dl
         dec       dh
         call      ukaz				; show sqaure up
         add       dh,2
         call      ukaz				; show square down
         jmp       vstup			; next command

; ------ show one square DX

ukaz:    push      dx
         cmp       dl,39
         jae       ukaz4			; invalid position
         call      readch			; read character on position DX
         cmp       ax,word ptr cs:[znakv0]	; free square not-visible?
         jne       ukaz2
         mov       ax,word ptr cs:[znakv1]	; substitute with free square visible
         jmp       short ukaz3			; display character
ukaz2:   cmp       ax,word ptr cs:[znakp0]	; full square not-visible?
         jne       ukaz4
         mov       ax,word ptr cs:[znakp1]	; substitute with full square visible
ukaz3:   call      writech			; display character
ukaz4:   pop       dx
         ret

; ------ draw row of frame

linkaram:push      cx
         xor       dl,dl
         mov       cx,40			; row length
         call      writechr
         pop       cx
         ret

; ------ check character on position DX

testch:  call      readch			; read character from DX
         cmp       word ptr ds:[znakv0],ax	; free square not-visible
         je        testch3
         cmp       word ptr ds:[znakv1],ax	; free square visible
         je        testch3
         cmp       word ptr ds:[znakcil],ax	; destination square
         je        testch3
         stc					; flag - not free square
testch3: ret

; ------ read character from position DX

readch:  push      dx
         push      si
         push      ds

         mov       al,ds:[byteline]		; number of bytes per row
         mul       dh
         mov       dh,0
         add       ax,dx
         add       ax,dx			; add column
         mov       si,ax			; source address

         mov       ds,ds:[VRAM]			; videomemory segment
         cld
         lodsw

         pop       ds
         pop       si
         pop       dx
         ret

; ------ write text DS:SI on position dX

writetxt:push      si
         push      ax
writtxt2:lodsb
         or        al,al
         jz        writtxt3
         call      writech
         jmp       short writtxt2
writtxt3:pop       ax
         pop       si
         ret

; ------ write character on position DX

writech: push      cx
         mov       cx,1
         call      writechr
         pop       cx
         inc       dl
         ret

; ------ write CX characters on position DX

writechr:push      cx
         push      dx
         push      di
         push      es

         push      ax
         mov       al,ds:[byteline]		; number of bytes per row
         mul       dh
         mov       dh,0
         add       ax,dx
         add       ax,dx			; add column
         mov       di,ax			; destination address
         pop       ax

         mov       es,ds:[VRAM]			; videomemory segment
         cld
         rep       stosw

         pop       es
         pop       di
         pop       dx
         pop       cx
         ret

; ------ swich cursor off

kurzout: push      dx
         mov       dx,25*256
         call      gotoxy
         pop       dx
         ret

; ------ set cursor position

gotoxy:  push      ax
         push      bx
         push      dx
         xor       bx,bx
         mov       ah,2
         call      int10
         pop       dx
         pop       bx
         pop       ax
         ret

; ------ INT 10h service

int10:   push      si
         push      di
         push      bp
         push      ds
         push      es
         int       10h
         pop       es
         pop       ds
         pop       bp
         pop       di
         pop       si
         ret

; ------ random number

random0: push      ax
         push      bx
         push      cx

         mov       ax,word ptr cs:[randomr]
         mov       bx,word ptr cs:[randomr+2]
         mov       cx,ax
         mul       word ptr cs:[randomd]
         shl       cx,1
         shl       cx,1
         shl       cx,1
         add       ch,cl
         add       dx,cx
         add       dx,bx
         shl       bx,1
         shl       bx,1
         add       dx,bx
         add       dh,bl
         mov       cl,5
         shl       bx,cl
         add       dh,bl
         add       ax,1
         adc       dx,0
         mov       word ptr cs:[randomr],ax
         mov       word ptr cs:[randomr+2],dx

         pop       cx
         pop       bx
         pop       ax
         ret

